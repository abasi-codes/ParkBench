defmodule ParkBenchWeb.Plugs.RequireAuth do
  @moduledoc "Ensures the current user is authenticated. Redirects to / if not."
  import Plug.Conn
  import Phoenix.Controller

  def init(opts), do: opts

  def call(conn, _opts) do
    if conn.assigns[:current_user] do
      conn
    else
      conn
      |> put_flash(:error, "You must log in to access this page.")
      |> redirect(to: "/")
      |> halt()
    end
  end
end
